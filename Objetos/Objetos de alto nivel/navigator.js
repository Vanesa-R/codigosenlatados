navigator.appVersion; // Devuelve la versión del navegador
navigator.language; // Devuelve el lenguaje del navegador
navigator.cookieEnabled // Indica sin las cookies están habilitadas en el navegador
navigator.geolocation // Devuelve un objeto que puede utilizarse para localizar la posición del usuario
